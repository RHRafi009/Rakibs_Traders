    private void txtFieldBuyerNameFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtFieldBuyerNameFocusGained
        // TODO add your handling code here:
        if( txtFieldBuyerName.getText().equals("Enter Buyer Name") ){
            txtFieldBuyerName.setText("");
            txtFieldBuyerName.setForeground(Color.black);
        }
        
        if(txtFieldVendorIDValue.getText().equals("Enter Vendor ID") || txtFieldVendorIDValue.getText().equals("")){
            vendorInsertFlag = 1;
        }else{
            Connection con = DBConnectionProvider.getDBConnection();
            String query = "SELECT * FROM vendors WHERE vendor_id = ?";
            try{
                PreparedStatement pstmt = con.prepareStatement(query);
                String vendorID = this.txtFieldVendorIDValue.getText();
                pstmt.setString(1,vendorID);
                ResultSet rs = pstmt.executeQuery();
                while(rs.next()){
                  this.txtFieldBuyerName.setText(rs.getString("vendor_name"));
                  this.txtFieldPhone1.setText(rs.getString("phone1"));
                  txtFieldPhone1.setForeground(Color.black);
                  this.txtFieldPhone2.setText(rs.getString("phone2"));
                  txtFieldPhone2.setForeground(Color.black);
                  this.txtFieldEmail.setText(rs.getString("email"));
                  txtFieldEmail.setForeground(Color.black);
                  this.txtFieldShopName.setText(rs.getString("shop_name"));
                  txtFieldShopName.setForeground(Color.black);
                  this.txtFieldDealerships.setText(rs.getString("dealerships"));
                  txtFieldDealerships.setForeground(Color.black);
                  this.txtFieldAddress.setText(rs.getString("address"));
                  txtFieldAddress.setForeground(Color.black);
                  this.jLabelBusinessNumber.setText(rs.getString("no_of_bus"));
                }
            }catch(Exception ex){
                System.out.println("Failed to get DBConn:: "+ex.getMessage());
                NoConnection no = new NoConnection();
                RakibsTraders.popUp(no);
            }
        }
        
    }//GEN-LAST:event_txtFieldBuyerNameFocusGained

