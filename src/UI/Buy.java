    private String insertVendor(){
        Connection con = DBConnectionProvider.getDBConnection();
        String query = "INSERT INTO `vendors` (`vendor_id`, `vendor_name`, `shop_name`, `address`, `phone1`, `phone2`, `email`, `dealerships`, `no_of_bus`) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
        String vendorId = "0";
        try{
            PreparedStatement pstmt = con.prepareStatement(query);
            String vendorName = this.txtFieldBuyerName.getText();
            String phone1 = this.txtFieldPhone1.getText();
            vendorId = Functions.vendorIDGenerator(phone1.substring(5,11));//call id generator here
            String shopName = this.txtFieldShopName.getText();
            
            //String product_id = "testing1";
            String address = this.txtFieldAddress.getText();
            String phone2 = this.txtFieldPhone2.getText();
            String email = this.txtFieldEmail.getText();
            String dealerships = this.txtFieldDealerships.getText();
            Integer noOfBus = 0; 
            pstmt.setString(1, vendorId);
            pstmt.setString(2, vendorName);
            pstmt.setString(3, shopName);
            pstmt.setString(4, address);
            pstmt.setString(5, phone1);
            pstmt.setString(6, phone2);
            pstmt.setString(7, email);
            pstmt.setString(8, dealerships);
            pstmt.setString(9, ""+noOfBus);
            pstmt.executeUpdate();
        } catch(Exception ex){
            System.out.println("Failed to get DBConn:: "+ex.getMessage());
            NoConnection no = new NoConnection();
            RakibsTraders.popUp(no);
        }
        return vendorId;
    }  

   
    
