    private void txtProductIDFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtProductIDFocusGained
        // TODO add your handling code here:
        SimpleDateFormat dFormat = new SimpleDateFormat("dd/MM/yyyy");
        Date d = new Date();
        String date = dFormat.format(d);
        if(txtFieldTimeAndDate2.getText().equals("")){
            Calendar cal = new GregorianCalendar();
            Integer sec = cal.get(Calendar.SECOND);
            Integer hour = cal.get(Calendar.HOUR);
            if(hour==0)
                hour=12;
            Integer min = cal.get(Calendar.MINUTE);
            Integer am_pm = cal.get(Calendar.AM_PM);
            if(am_pm == 0){
                String timeAndDate = hour + ":" + min + ":" + sec + " " + "AM" + " " + date;
                txtFieldTimeAndDate2.setText(timeAndDate);
            }else {
                String timeAndDate = hour + ":" + min + ":" + sec + " " + "PM" + " " + date;
                txtFieldTimeAndDate2.setText(timeAndDate);
            }
        }
        String id = Functions.billIDGenerator(date);
        lblBillingIDValue.setText(id);
    }//GEN-LAST:event_txtProductIDFocusGained

