    private void txtProductModelValueFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtProductModelValueFocusGained
        // TODO add your handling code here:
        //String companyName = this.txtFieldCustomerIDValue.getText();
        Connection con = DBConnectionProvider.getDBConnection();
        String query1 = "Select company_name, model, item_unit,selling_price FROM products WHERE products_id = ?";
        String query2 = "Select * From stock WHERE products_id = ?";
//        initComboModel(companyName);
        try{
            PreparedStatement pstmt1 = con.prepareStatement(query1);
            String productID = this.txtProductID.getText();
//            System.err.println(productID);
            pstmt1.setString(1,productID);
            ResultSet rs1 = pstmt1.executeQuery();
            
            while( rs1.next()) {
                this.jLabelCompanyName.setText(rs1.getString("company_name"));
                this.jLabelItemUnit1.setText(rs1.getString("item_unit"));
                this.txtProductModelValue.setText(rs1.getString("model"));
                this.jTextFieldItemSellPrice.setText(rs1.getString("selling_price"));
            }
            this.txtProductModelValue.setEditable(false);
            
        }catch( Exception ex){
            System.out.println("Faild to get DBConn:: " + ex.getMessage());
            NoConnection no = new NoConnection();
            RakibsTraders.popUp(no);
            
        }
        
        try{
            PreparedStatement pstmt2 = con.prepareStatement(query2);
            String productID = this.txtProductID.getText();
            pstmt2.setString (1,productID);
            ResultSet rs2 = pstmt2.executeQuery(); 
            
            while( rs2.next() ){
                this.jLabelAvailableQtyValue.setText(Integer.toString(rs2.getInt("left")));
            }
            
        }catch(Exception ex){
            System.out.println("Failed to get DBConn:: "+ex.getMessage());
        }
        

    }//GEN-LAST:event_txtProductModelValueFocusGained

